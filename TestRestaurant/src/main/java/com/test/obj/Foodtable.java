package com.test.obj;

public class Foodtable {
    private int id;
    private String stat;
    private int speci;
    private int nowspeci;
    private String createtime;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStat() {
        return stat;
    }

    public void setStat(String stat) {
        this.stat = stat;
    }

    public int getSpeci() {
        return speci;
    }

    public void setSpeci(int speci) {
        this.speci = speci;
    }

    public int getNowspeci() {
        return nowspeci;
    }

    public void setNowspeci(int nowspeci) {
        this.nowspeci = nowspeci;
    }

    public String getCreatetime() {
        return createtime;
    }

    public void setCreatetime(String createtime) {
        this.createtime = createtime;
    }
}
